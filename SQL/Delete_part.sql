ALTER PROCEDURE [dbo].[delete_part]

	@id int
AS

BEGIN
SET NOCOUNT ON;
BEGIN TRY
	BEGIN TRANSACTION

		DELETE FROM [dbo].[table_descendant]
		WHERE id_main = @id;

		DELETE FROM [dbo].[delete_part]
		WHERE id = @id;


		COMMIT TRANSACTION
		RETURN 3
END TRY
BEGIN CATCH
	ROLLBACK TRANSACTION
	RETURN 7
END CATCH

END
